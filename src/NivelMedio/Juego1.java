    private void pintarImagen(JLabel jLabel1, String ruta) {

        this.imagen = new ImageIcon(ruta);
        this.icono = new ImageIcon(
                this.imagen.getImage().getScaledInstance(
                        jLabel1.getWidth(),
                        jLabel1.getHeight(),
                        Image.SCALE_DEFAULT
                )
        );
        jLabel1.setIcon(this.icono);
        this.repaint();

    }


